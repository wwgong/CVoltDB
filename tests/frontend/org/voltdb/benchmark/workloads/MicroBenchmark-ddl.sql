CREATE TABLE ALL_TYPES
(
  INT_ITEM INTEGER NOT NULL,
  BYTE_ITEM TINYINT NOT NULL,
  SHORT_ITEM SMALLINT NOT NULL,
  LONG_ITEM BIGINT NOT NULL,
  DOUBLE_ITEM FLOAT,
  DECIMAL_ITEM DECIMAL,
  TIME_ITEM TIMESTAMP,
  STRING_ITEM VARCHAR(5) NOT NULL,
  PRIMARY KEY (SHORT_ITEM)
);

CREATE INDEX INT_TREE_INDEX
ON ALL_TYPES (INT_ITEM);

CREATE INDEX BYTE_TREE_INDEX
ON ALL_TYPES (BYTE_ITEM);

CREATE INDEX LONG_TREE_INDEX
ON ALL_TYPES (LONG_ITEM);

CREATE INDEX DOUBLE_TREE_INDEX
ON ALL_TYPES (DOUBLE_ITEM);

CREATE INDEX DECIMAL_TREE_INDEX
ON ALL_TYPES (DECIMAL_ITEM);

CREATE INDEX TIME_TREE_INDEX
ON ALL_TYPES (TIME_ITEM);

CREATE INDEX STRING_TREE_INDEX
ON ALL_TYPES (STRING_ITEM);
